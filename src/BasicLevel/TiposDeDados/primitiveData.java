package BasicLevel.TiposDeDados;

public class primitiveData {
    public static void main(String[] args) {
        // Java is very verbose
        int idade = 21;
        double altura = 1.76;
        char inicial = 'e';
        boolean vivo = true;
        Long dinheiros = 999999999L; // começa com uppercase e termina com uppercase L

        System.out.println("Então você tem os valores: " + idade + altura + inicial + vivo + dinheiros);
    }
}
